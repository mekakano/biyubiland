    ),
    gridData: FlGridData(
      show: false,
    ),
    titlesData: FlTitlesData(
      bottomTitles: SideTitles(
        showTitles: true,
        reservedSize: 22,
        getTextStyles: (context, value) => const TextStyle(
          color: Color(0xff72719b),
          fontWeight: FontWeight.bold,
          fontSize: 16,
        ),
        margin: 10,
        getTitles: (value) {
          switch (value.toInt()) {
            case 1:
              return 'JAN';
            case 2:
              return 'FEB';
            case 3:
              return 'MAR';
            case 4:
              return 'APR';
            case 5:
              return 'MEI';
            case 6:
              return 'JUNI';
